# How to format your print out statements

name = "John"

print(f"Hello {name}.")

val = 3.0

print(f"Val = {val + 2}")

PI = 3.14159

print(f"Value of PI is {PI:.2f}")

num = 1234.5678
print(f"Value of num is {num:.2e}")

discount = 0.5
print(f"Discount is {discount:.0%}")


num = 1234.5678
print(f"Value of num is {num: ^12,.2f}")
